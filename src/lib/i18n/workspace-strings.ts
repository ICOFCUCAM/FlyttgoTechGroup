/**
 * Workspace-specific string table.
 *
 * The marketing site has its own deep i18n tree under
 * src/lib/i18n/dictionaries.ts. The accounting workspace uses this
 * separate, smaller table because most of its surface is technical
 * (account codes, journal numbers, mono labels) and doesn't translate
 * — only the headings, primary actions and statutory-export labels
 * benefit from a Norwegian rendering.
 *
 * The brief calls out support for English + Norwegian today, with a
 * structure that can extend to UK English / US English later. UK and
 * US dialects share the canonical `en` key and override only when a
 * spelling actually diverges (organisation vs organization etc.).
 */

