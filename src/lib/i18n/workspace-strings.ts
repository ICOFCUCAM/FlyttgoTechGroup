export type WorkspaceStringKey = keyof typeof WORKSPACE_STRINGS.en;

export function isWorkspaceLang(value: unknown): value is WorkspaceLang {
  return typeof value === 'string' && (WORKSPACE_LANGS as readonly string[]).includes(value);
}

export function t(lang: WorkspaceLang, key: WorkspaceStringKey): string {
  return WORKSPACE_STRINGS[lang][key] ?? WORKSPACE_STRINGS.en[key];
}
